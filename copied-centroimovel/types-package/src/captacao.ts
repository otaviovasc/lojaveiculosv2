export const CreatePropertyAssignmentSchema = z.object({
  propertyId: z.string().min(1),
  corretorNotes: z.string().optional(),
});

export const UpdatePropertyAssignmentSchema = z.object({
  status: z.enum(["APPROVED", "REJECTED", "REVOKED"]),
  reason: z.string().optional(),
});

export const RecordClosingSchema = z.object({
  assignmentId: z.string().min(1),
  salePrice: z.number().positive(),
  closingDate: z.coerce.date(),
  type: z.enum(["VENDA", "ALUGUEL"]),
  buyerName: z.string().min(1),
  buyerCpf: z.string().optional(),
  buyerPhone: z.string().optional(),
  financingType: z.enum(["CASH", "FINANCING", "FGTS", "MIXED"]),
});
