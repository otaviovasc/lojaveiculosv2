import { z } from "zod";

export const CreateOwnerSubmissionSchema = z.object({
  ownerName: z.string().min(1),
  ownerEmail: z.string().email(),
  ownerPhone: z.string().min(1),
  ownerWhatsApp: z.string().optional(),
  ownerCpf: z.string().optional(),
  propertyType: z.string().min(1),
  propertyAddress: z.string().min(1),
  propertyCity: z.string().min(1),
  propertyState: z.string().default("SP"),
  price: z.number().optional(),
  rentPrice: z.number().optional(),
  cep: z.string().optional(),
  propertyStreet: z.string().optional(),
  propertyNumber: z.string().optional(),
  propertyComplement: z.string().optional(),
  propertyNeighborhood: z.string().optional(),
  propertyDescription: z.string().optional(),
  bedrooms: z.number().optional(),
  bathrooms: z.number().optional(),
  parkingSpots: z.number().optional(),
  area: z.number().optional(),
  purpose: z.string().default("VENDA"),
  photos: z.array(z.string()).optional(),
  preferredContact: z.string().default("WHATSAPP"),
  urgency: z.string().default("NORMAL"),
  howDidYouHear: z.string().optional(),
});

