async function readTable(table) {
  const columns = table.columns.map(quoteIdentifier).join(", ");
  const orderBy = table.orderBy.map(quoteIdentifier).join(", ");
  return sql.unsafe(
    `select ${columns} from ${quoteIdentifier(table.name)} order by ${orderBy}`,
  );
}

function renderTable(table, rows) {
  if (rows.length === 0) return `-- ${table.name}: no rows\n`;
  const chunks = chunk(rows, 500).map((items) =>
    renderInsertChunk(table, items),
  );
  return [`-- ${table.name}: ${rows.length} rows`, ...chunks, ""].join("\n");
}

function renderInsertChunk(table, rows) {
  const columns = table.columns.map(quoteIdentifier).join(", ");
  const values = rows
    .map((row) => {
      const rowValues = table.columns.map((column) => formatValue(row[column]));
      return `  (${rowValues.join(", ")})`;
    })
    .join(",\n");
  return [
    `INSERT INTO ${quoteIdentifier(table.name)} (${columns})`,
    "VALUES",
    `${values};`,
  ].join("\n");
}

function chunk(items, size) {
  const chunks = [];
  for (let index = 0; index < items.length; index += size) {
    chunks.push(items.slice(index, index + size));
  }
  return chunks;
}

function quoteIdentifier(value) {
  return `"${value.replaceAll('"', '""')}"`;
}

function formatValue(value) {
  if (value === null || value === undefined) return "null";
  if (value instanceof Date) {
    return `${formatString(value.toISOString())}::timestamp with time zone`;
  }
  if (typeof value === "boolean") return value ? "true" : "false";
  if (typeof value === "number") return String(value);
  return formatString(String(value));
}

function formatString(value) {
  return `'${value.replaceAll("'", "''")}'`;
}
